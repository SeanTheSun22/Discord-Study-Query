DROP TABLE IF EXISTS Guilds;
DROP TABLE IF EXISTS Categories;
DROP TABLE IF EXISTS Channels;
DROP TABLE IF EXISTS Threads;
CREATE TABLE Guilds (GuildID BIGINT PRIMARY KEY, test INT);
CREATE TABLE Categories (CategoryID BIGINT PRIMARY KEY, test INT);
CREATE TABLE Channels (ChannelID BIGINT PRIMARY KEY, PingRoleID BIGINT, ModeratorRoleID BIGINT);
CREATE TABLE Threads (ThreadID BIGINT PRIMARY KEY, OriginalCreatorID BIGINT);